// Cree "num" (una lista de números)
const num: number[] = [20, 14, 8, 0, 5, 19, 24];

// muestro en la consola un mensaje para que el usuario sepa qué va a ver
console.log("Estos son los valores que tiene num: ");

// Usando un for, recorremos la lista y mostramos cada numero en la consola
for (let i = 0; i < num.length; i++) {
  console.log(num[i]); // Mostramos el número actual en la consola
}
